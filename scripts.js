  // Initialize translations
  const savedLang = localStorage.getItem("preferredLanguage");
  const browserLang = navigator.language.split("-")[0];
  const defaultLang = translations[savedLang]
    ? savedLang
    : translations[browserLang]
    ? browserLang
    : "en";

